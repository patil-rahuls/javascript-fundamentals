// REVERSE: (Mutates the original array and returns the reversed array)
const arr = [11,22,33,44,55,66];
const newArr = arr.reverse();
console.log(newArr);       // [66,55,44,33,22,11]
console.log(arr);          // [66,55,44,33,22,11]
